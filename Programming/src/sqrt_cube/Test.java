package sqrt_cube;

public interface Test {
	public static void square(int number) {
		int square = number * number;
		System.out.println("square of the number" + number + " is " + square);

	}

	public static void squareroot(int number) {
		int squareroot=0;
		for(int i=0;i<=number;i++)
		{
			if((i*i)<=number) {
				squareroot=i;
			}
		}
		System.out.println("squareroot of the number"+number+" is "+squareroot);
	}

}
